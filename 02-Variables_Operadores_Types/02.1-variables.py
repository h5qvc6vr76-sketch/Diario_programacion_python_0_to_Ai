# en python las variables son como una caja etiquetada que 
# almacena datos de diferentes types


# para declarar variables le asignas un valor a un indentificador 
# con el simbolo =, entonces a la izquierda el nombre de la variable y a la derecha el type y valor asignado



#----- EJEMPLO -----
nombre = " Mario "
edad = 27

# el nombre de las variables son nombre y edad, con los valores asignados como Mario y 27 respectivamente.
# los nombres de variable solo pueden empezar con una letra o un "_"

